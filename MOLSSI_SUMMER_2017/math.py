"""
A smal set of function for doing math operations.
"""

# Write a function that add two values
def add(a,b):
    """
    Function that adds two numbers
    """
    return a + b

# Write a function that multiply two values
def mult(a,b):
    """
    Function that multiply two numbers
    """
    return a * b

# Write a function that subtract two values
def sub(a,b):
    """
    Function that subtract two numbers
    """
    return a - b

# Write a function that divide two values
def div(a,b):
    """
    Function that divide two numbers
    """
    return a / b
